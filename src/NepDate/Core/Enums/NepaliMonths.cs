namespace NepDate
{
    /// <summary>
    /// Represents the twelve months of the Bikram Sambat (Nepali) calendar.
    /// Month numbers are 1-based and correspond to the numeric <see cref="NepaliDate.Month"/> property.
    /// </summary>
    public enum NepaliMonths
    {
        /// <summary>First month of the Nepali year (mid-April to mid-May).</summary>
        Baishakh = 1,
        /// <summary>Second month of the Nepali year (mid-May to mid-June).</summary>
        Jestha,
        /// <summary>Third month of the Nepali year (mid-June to mid-July).</summary>
        Ashad,
        /// <summary>Fourth month of the Nepali year (mid-July to mid-August).</summary>
        Shrawan,
        /// <summary>Fifth month of the Nepali year (mid-August to mid-September).</summary>
        Bhadra,
        /// <summary>Sixth month of the Nepali year (mid-September to mid-October).</summary>
        Ashoj,
        /// <summary>Seventh month of the Nepali year (mid-October to mid-November).</summary>
        Kartik,
        /// <summary>Eighth month of the Nepali year (mid-November to mid-December).</summary>
        Mangsir,
        /// <summary>Ninth month of the Nepali year (mid-December to mid-January).</summary>
        Poush,
        /// <summary>Tenth month of the Nepali year (mid-January to mid-February).</summary>
        Magh,
        /// <summary>Eleventh month of the Nepali year (mid-February to mid-March).</summary>
        Falgun,
        /// <summary>Twelfth and final month of the Nepali year (mid-March to mid-April).</summary>
        Chaitra,
    }
}
